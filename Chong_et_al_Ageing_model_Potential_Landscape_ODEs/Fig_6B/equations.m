function dy = equations( t, y, params)

dy=zeros(13,1);

DSB=0.3;
GS=0.2;
a=1.5;
b=0.05;
k=1;

% P53
dy(1)=a*y(13)^4/(0.5^4+y(13)^4)+a*y(1)^4/(0.5^4+y(1)^4)+b*0.5^4/(0.5^4+y(2)^4)+b*0.5^4/(0.5^4+y(3)^4)+a*y(4)^4/(0.5^4+y(4)^4)-k*y(1); 

% Mdm2
dy(2)=b*0.5^4/(0.5^4+y(13)^4)+a*y(1)^4/(0.5^4+y(1)^4)+a*y(7)^4/(0.5^4+y(7)^4)+b*0.5^4/(0.5^4+y(3)^4)-k*y(2); 

% Wip1
dy(3)=a*y(1)^4/(0.5^4+y(1)^4)-k*y(3);

% ATM
dy(4)=a*y(4)^4/(0.5^4+y(4)^4)+a*DSB^4/(0.5^4+DSB^4)+b*0.5^4/(0.5^4+y(3)^4)-k*y(4); 

% P21
dy(5)=a*y(1)^4/(0.5^4+y(1)^4)+b*0.5^4/(0.5^4+y(7)^4)-k*y(5); 

% PTEN
dy(6)=a*y(1)^4/(0.5^4+y(1)^4)-k*y(6); 

% AKT
dy(7)=b*0.5^4/(0.5^4+y(6)^4)-k*y(7); 

% Myc
dy(8)=a*GS^4/(0.5^4+GS^4)-k*y(8); 

% E2F made changes for p21 inhibits E2F (instead of p21 activating E2F)
dy(9)=b*0.5^4/(0.5^4+y(5)^4)+a*y(9)^4/(0.5^4+y(9)^4)+b*0.5^4/(0.5^4+y(10)^4)+a*y(8)^4/(0.5^4+y(8)^4)-k*y(9);

% RB
dy(10)=a*y(5)^4/(0.5^4+y(5)^4)+b*0.5^4/(0.5^4+y(12)^4)+b*0.5^4/(0.5^4+y(11)^4)-k*y(10);

% CycE
dy(11)=a*y(9)^4/(0.5^4+y(9)^4)+b*0.5^4/(0.5^4+y(5)^4)-k*y(11); 

% CycD
dy(12)=a*y(8)^4/(0.5^4+y(8)^4)+a*GS^4/(0.5^4+GS^4)+b*0.5^4/(0.5^4+y(5)^4)-k*y(12); 

% ARF
dy(13)=a*y(8)^4/(0.5^4+y(8)^4)-k*y(13); 
end
